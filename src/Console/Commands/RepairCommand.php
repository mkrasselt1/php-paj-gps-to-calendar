<?php

namespace PajGpsCalendar\Console\Commands;

use Symfony\Component\Console\Command\Command;
use Symfony\Component\Console\Input\InputInterface;
use Symfony\Component\Console\Output\OutputInterface;
use Symfony\Component\Console\Input\InputOption;
use PajGpsCalendar\Services\ConfigService;
use PajGpsCalendar\Services\PajApiService;
use PajGpsCalendar\Services\CrmService;
use PajGpsCalendar\Services\CalendarService;
use PajGpsCalendar\Services\GeoService;
use PajGpsCalendar\Services\BlindSpotService;
use PajGpsCalendar\Helpers\PathHelper;
use Monolog\Logger;
use Monolog\Handler\StreamHandler;

/**
 * Repairs the Google Calendar by reconciling it against PAJ GPS historical data.
 *
 * For each vehicle and each day in the requested range the command:
 *  1. Fetches actual visits from the PAJ API.
 *  2. Lists existing calendar entries for that day.
 *  3. Deletes duplicate / orphan entries created by this system.
 *  4. Creates (or re-creates) the single canonical entry per visit.
 *
 * "Canonical" means the event whose Google Calendar ID equals
 *  md5(generateStableEventId(vehicle, customer, visitStartTime)).
 * That ID is deterministic, so re-running the command is idempotent.
 */
class RepairCommand extends Command
{
    protected static $defaultName = 'repair-calendar';

    /** Text present in every event description generated by this system */
    private const MARKER = 'PAJ GPS';

    private ConfigService $config;
    private Logger $logger;

    protected function configure(): void
    {
        $this
            ->setDescription('Repariert Kalendereinträge: löscht Duplikate, führt Einträge zusammen und erstellt fehlende Einträge neu')
            ->addOption('days', null, InputOption::VALUE_REQUIRED,
                'Anzahl der Tage in der Vergangenheit (Standard: 30, max. ~365)', 30)
            ->addOption('from', null, InputOption::VALUE_REQUIRED,
                'Startdatum (Format: YYYY-MM-DD oder DD.MM.YYYY)')
            ->addOption('to', null, InputOption::VALUE_REQUIRED,
                'Enddatum (Format: YYYY-MM-DD oder DD.MM.YYYY)')
            ->addOption('vehicle-id', null, InputOption::VALUE_REQUIRED,
                'Nur ein bestimmtes Fahrzeug bearbeiten')
            ->addOption('dry-run', 'd', InputOption::VALUE_NONE,
                'Zeigt nur an was geändert würde, ohne tatsächliche Änderungen')
            ->addOption('delete-orphans', null, InputOption::VALUE_NONE,
                'Löscht Einträge im Kalender ohne zugehörigen PAJ-Besuch')
            ->addOption('force-update', 'f', InputOption::VALUE_NONE,
                'Aktualisiert korrekt vorhandene Einträge trotzdem mit den neuesten PAJ-Zeiten');
    }

    protected function execute(InputInterface $input, OutputInterface $output): int
    {
        try {
            $this->initializeServices();

            $dryRun       = (bool) $input->getOption('dry-run');
            $deleteOrphans = (bool) $input->getOption('delete-orphans');
            $forceUpdate  = (bool) $input->getOption('force-update');
            $vehicleIdFilter = $input->getOption('vehicle-id');

            $output->writeln('<info>PAJ GPS to Calendar – Kalender-Reparatur</info>');
            if ($dryRun) {
                $output->writeln('<comment>Dry-Run Modus – keine Änderungen werden gespeichert</comment>');
            }

            // ── Services ──────────────────────────────────────────────────────
            $pajApi    = new PajApiService($this->config, $this->logger);
            $geo       = new GeoService($this->config, $this->logger);
            $crm       = new CrmService($this->config, $this->logger, $geo);
            $calendar  = new CalendarService($this->config, $this->logger);
            $blindSpot = new BlindSpotService($this->config);

            $timezone = new \DateTimeZone($this->config->get('settings.timezone', 'Europe/Berlin'));

            // ── Date range ────────────────────────────────────────────────────
            [$rangeStart, $rangeEnd] = $this->resolveDateRange($input, $timezone);

            $output->writeln(sprintf('Zeitraum: %s – %s',
                $rangeStart->format('d.m.Y'),
                $rangeEnd->format('d.m.Y')
            ));

            // ── CRM connection test ───────────────────────────────────────────
            if (!$crm->testCrmConnection()) {
                $output->writeln('<error>CRM-Verbindung fehlgeschlagen</error>');
                return Command::FAILURE;
            }

            // ── Vehicles ──────────────────────────────────────────────────────
            $vehicles = $pajApi->getVehicles();
            if ($vehicleIdFilter) {
                $vehicles = array_values(array_filter($vehicles, fn($v) => $v['id'] === $vehicleIdFilter));
                if (empty($vehicles)) {
                    $output->writeln("<error>Fahrzeug '{$vehicleIdFilter}' nicht gefunden</error>");
                    return Command::FAILURE;
                }
            }

            $proximityThreshold  = $this->config->get('settings.proximity_threshold_meters', 500);
            $minimumVisitDuration = $this->config->get('settings.minimum_visit_duration_minutes', 2);

            // Global stats
            $totalKept    = 0;
            $totalDeleted = 0;
            $totalCreated = 0;
            $totalOrphans = 0;

            // ── Process each vehicle ──────────────────────────────────────────
            foreach ($vehicles as $vehicle) {
                $output->writeln('');
                $output->writeln(sprintf('<info>Fahrzeug: %s (%s)</info>', $vehicle['name'], $vehicle['id']));

                // 1. Fetch all PAJ visits for the full range in one call
                $output->writeln('  Lade PAJ-Besuchsdaten …');
                $pajVisits = $pajApi->analyzeHistoricalVisits(
                    $vehicle['id'],
                    $rangeStart->getTimestamp(),
                    $rangeEnd->getTimestamp(),
                    $minimumVisitDuration
                );
                $output->writeln(sprintf('  %d Besuche in PAJ-Daten gefunden', count($pajVisits)));

                // 2. Fetch all calendar events for the full range in one call
                $output->writeln('  Lade Kalendereinträge …');
                $allCalEvents = $calendar->listEventsInRange($rangeStart, $rangeEnd);

                // Keep only events created by this system.
                // Primary check: ID starts with our stable-ID prefix paj-gps-{vehicleId}-
                //   → reliable structural match, works even if description was overwritten
                //     by updateEntryWithEndTime (which used to drop the marker).
                // Fallback: marker in description + vehicle name in summary
                //   → catches any legacy format entries.
                $vehicleIdPrefix = 'paj-gps-' . $vehicle['id'] . '-';
                $ourCalEvents = array_values(array_filter(
                    $allCalEvents,
                    fn($e) => str_starts_with($e['id'], $vehicleIdPrefix)
                             || (str_contains($e['description'], self::MARKER)
                                 && str_contains($e['summary'], $vehicle['name']))
                ));
                $output->writeln(sprintf('  %d bestehende Einträge für dieses Fahrzeug im Kalender', count($ourCalEvents)));

                // 3. Build expected visits: resolve nearest customer for each PAJ visit
                $expectedVisits = [];
                foreach ($pajVisits as $visit) {
                    if ($blindSpot->isInBlindSpot($visit['latitude'], $visit['longitude'])) {
                        continue;
                    }
                    $nearby = $crm->findCustomersNearLocation(
                        $visit['latitude'], $visit['longitude'], $proximityThreshold
                    );
                    if (empty($nearby)) {
                        continue;
                    }
                    // Pick the closest customer
                    usort($nearby, fn($a, $b) => $a['distance_meters'] <=> $b['distance_meters']);
                    $customer = $nearby[0];

                    $vehicleData = [
                        'id'        => $vehicle['id'],
                        'name'      => $vehicle['name'],
                        'latitude'  => $visit['latitude'],
                        'longitude' => $visit['longitude'],
                        'speed'     => 0,
                    ];

                    $stableId     = $calendar->generateStableEventId($vehicleData, $customer, $visit['start_time']);
                    $canonicalId  = $calendar->getCanonicalEventId($stableId);

                    $expectedVisits[] = [
                        'vehicle'      => $vehicleData,
                        'customer'     => $customer,
                        'start_time'   => $visit['start_time'],
                        'end_time'     => $visit['end_time'],
                        'distance'     => $customer['distance_meters'],
                        'stable_id'    => $stableId,
                        'canonical_id' => $canonicalId,
                        'label'        => sprintf('%s bei %s (%s)', $vehicle['name'], $customer['customer_name'], $visit['start_time_formatted']),
                    ];
                }

                // 4. Reconcile: for each expected visit find matching calendar events
                //    A calendar event "matches" if it shares the vehicle+customer name combo
                //    and its start time is within 3 hours of the PAJ visit start.
                $matchedCalEventIds = []; // track which cal events were claimed by a visit

                $vKept = $vDeleted = $vCreated = 0;

                foreach ($expectedVisits as $ev) {
                    $matchingEvents = $this->findMatchingCalendarEvents(
                        $ourCalEvents, $ev['vehicle'], $ev['customer'], $ev['start_time']
                    );

                    $hasCanonical = false;
                    $toDelete     = [];

                    foreach ($matchingEvents as $calEvent) {
                        $matchedCalEventIds[] = $calEvent['id'];
                        if ($calEvent['id'] === $ev['canonical_id']) {
                            $hasCanonical = true;
                        } else {
                            $toDelete[] = $calEvent;
                        }
                    }

                    $duplicateCount = count($toDelete);

                    if ($hasCanonical && !$forceUpdate && $duplicateCount === 0) {
                        // Already correct, nothing to do
                        $this->printLine($output, '⏭️ ', $ev['label'], 'bereits korrekt');
                        $vKept++;
                        continue;
                    }

                    // Delete duplicates / old-format events
                    if ($duplicateCount > 0) {
                        $this->printLine($output, '🗑️ ', $ev['label'],
                            sprintf('%d Duplikat(e) werden gelöscht', $duplicateCount));
                        if (!$dryRun) {
                            foreach ($toDelete as $dup) {
                                $calendar->deleteEvent($dup['id']);
                                $vDeleted++;
                            }
                        }
                    }

                    // Create / re-create canonical event
                    if (!$hasCanonical || $forceUpdate) {
                        $action = ($hasCanonical && $forceUpdate) ? 'wird aktualisiert' : 'wird erstellt';
                        $this->printLine($output, '✅', $ev['label'], $action);

                        if (!$dryRun) {
                            $calendar->createHistoricalEntry(
                                $ev['vehicle'],
                                $ev['customer'],
                                $ev['distance'],
                                $ev['start_time'],
                                $ev['end_time'],
                                $forceUpdate // if force, delete+recreate in CalendarService
                            );
                            $vCreated++;
                        }
                    } elseif ($hasCanonical) {
                        $vKept++;
                    }
                }

                // 5. Handle orphan calendar events (in calendar but no matching PAJ visit)
                $orphans = array_values(array_filter(
                    $ourCalEvents,
                    fn($e) => !in_array($e['id'], $matchedCalEventIds, true)
                ));

                if (!empty($orphans)) {
                    $output->writeln(sprintf(
                        '  <comment>%d verwaiste Einträge ohne PAJ-Besuch gefunden</comment>',
                        count($orphans)
                    ));

                    foreach ($orphans as $orphan) {
                        $orphanLabel = sprintf('%s (Google-ID: %s)', $orphan['summary'], substr($orphan['id'], 0, 8) . '…');

                        if ($deleteOrphans) {
                            $this->printLine($output, '🗑️ ', $orphanLabel, 'verwaist – wird gelöscht');
                            if (!$dryRun) {
                                $calendar->deleteEvent($orphan['id']);
                                $vDeleted++;
                            }
                        } else {
                            $this->printLine($output, '⚠️ ', $orphanLabel, 'verwaist (verwende --delete-orphans zum Löschen)');
                        }
                        $totalOrphans++;
                    }
                }

                $output->writeln(sprintf(
                    '  Ergebnis: %d behalten, %d gelöscht, %d erstellt',
                    $vKept, $vDeleted, $vCreated
                ));

                $totalKept    += $vKept;
                $totalDeleted += $vDeleted;
                $totalCreated += $vCreated;
            }

            // ── Summary ───────────────────────────────────────────────────────
            $output->writeln('');
            $output->writeln('<info>═══════════════════════════════════════════</info>');
            $output->writeln('<info>Reparatur abgeschlossen</info>');
            $output->writeln(sprintf('  Einträge behalten  : %d', $totalKept));
            $output->writeln(sprintf('  Duplikate gelöscht : %d', $totalDeleted));
            $output->writeln(sprintf('  Einträge erstellt  : %d', $totalCreated));
            if ($totalOrphans > 0) {
                $suffix = $deleteOrphans ? ' (gelöscht)' : ' (behalten, siehe --delete-orphans)';
                $output->writeln(sprintf('  Verwaiste Einträge : %d%s', $totalOrphans, $suffix));
            }
            if ($dryRun) {
                $output->writeln('<comment>(Dry-Run – keine Änderungen wurden gespeichert)</comment>');
            }

            return Command::SUCCESS;

        } catch (\Exception $e) {
            $output->writeln(sprintf('<error>Fehler: %s</error>', $e->getMessage()));
            $this->logger->error('repair-calendar fehlgeschlagen', ['error' => $e->getMessage()]);
            return Command::FAILURE;
        }
    }

    // ── Helpers ───────────────────────────────────────────────────────────────

    /**
     * Returns all calendar events whose summary contains both the vehicle name
     * and the customer name, and whose start time is within 3 hours of
     * the given PAJ visit start timestamp.
     *
     * A 3-hour window is generous enough to catch:
     *  - real-time events (which use "now" as start, not the actual visit start)
     *  - old-format historical events
     *  - new canonical events
     */
    private function findMatchingCalendarEvents(
        array $calEvents,
        array $vehicle,
        array $customer,
        int   $visitStartTs
    ): array {
        $windowSeconds = 3 * 3600;

        $vehicleIdPrefix = 'paj-gps-' . $vehicle['id'] . '-';
        return array_values(array_filter($calEvents, function ($e) use ($vehicle, $customer, $visitStartTs, $windowSeconds, $vehicleIdPrefix) {
            $vehicleMatch = str_contains($e['summary'], $vehicle['name'])
                || str_starts_with($e['id'], $vehicleIdPrefix);
            if (!$vehicleMatch) {
                return false;
            }
            if (!str_contains($e['summary'], $customer['customer_name'])) {
                return false;
            }
            return abs($e['start_ts'] - $visitStartTs) <= $windowSeconds;
        }));
    }

    /** Resolve date range from CLI options. */
    private function resolveDateRange(InputInterface $input, \DateTimeZone $tz): array
    {
        $fromOpt = $input->getOption('from');
        $toOpt   = $input->getOption('to');
        $days    = (int) $input->getOption('days');

        if ($fromOpt && $toOpt) {
            $start = $this->parseDate($fromOpt, $tz);
            $end   = $this->parseDate($toOpt, $tz);
            $end->setTime(23, 59, 59);
            if ($start > $end) {
                throw new \InvalidArgumentException('Start-Datum muss vor End-Datum liegen.');
            }
            return [$start, $end];
        }

        $end   = new \DateTime('now', $tz);
        $end->setTime(23, 59, 59);
        $start = (clone $end)->sub(new \DateInterval("P{$days}D"));
        $start->setTime(0, 0, 0);
        return [$start, $end];
    }

    private function parseDate(string $str, \DateTimeZone $tz): \DateTime
    {
        $str = trim($str);
        foreach (['Y-m-d', 'd.m.Y', 'd/m/Y', 'd-m-Y'] as $fmt) {
            $d = \DateTime::createFromFormat($fmt, $str, $tz);
            if ($d !== false) {
                return $d->setTime(0, 0, 0);
            }
        }
        $d = new \DateTime($str, $tz);
        return $d->setTime(0, 0, 0);
    }

    private function printLine(OutputInterface $output, string $icon, string $label, string $action): void
    {
        $output->writeln(sprintf('    %s %s → %s', $icon, $label, $action));
    }

    private function initializeServices(): void
    {
        $this->config = new ConfigService();
        $this->logger = new Logger('paj-gps-calendar');
        $logFile      = PathHelper::resolveProjectPath(
            $this->config->get('settings.log_file', 'logs/application.log')
        );
        $this->logger->pushHandler(
            new StreamHandler($logFile, $this->config->get('settings.log_level', 'info'))
        );
    }
}
